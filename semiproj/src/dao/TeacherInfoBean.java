package dao;

public class TeacherInfoBean {
	private String teCode;
	private String tePWD;
	public String getTeCode() {
		return teCode;
	}
	public void setTeCode(String teCode) {
		this.teCode = teCode;
	}
	public String getTePWD() {
		return tePWD;
	}
	public void setTePWD(String tePWD) {
		this.tePWD = tePWD;
	}
	public String getTeName() {
		return teName;
	}
	public void setTeName(String teName) {
		this.teName = teName;
	}
	public String getTeSub() {
		return teSub;
	}
	public void setTeSub(String teSub) {
		this.teSub = teSub;
	}
	public String getTeLevel() {
		return teLevel;
	}
	public void setTeLevel(String teLevel) {
		this.teLevel = teLevel;
	}
	public int getTeGrade() {
		return teGrade;
	}
	public void setTeGrade(int teGrade) {
		this.teGrade = teGrade;
	}
	public int getTeClass() {
		return teClass;
	}
	public void setTeClass(int teClass) {
		this.teClass = teClass;
	}
	public int getClNum() {
		return clNum;
	}
	public void setClNum(int clNum) {
		this.clNum = clNum;
	}
	private String teName;
	private String teSub;
	private String teLevel;
	private int teGrade;
	private int teClass;
	private int clNum;
}
